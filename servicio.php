	jQuery(window).scroll(function(){
		var scroll = $(document).scrollTop()+80;
		var servicio = $("#servicio-div").position().top;
		var comoTrabajo = $("#como-trabajo-div").position().top;
		var planes = $("#planes-div").position().top;
		var cita = $("#cita-div").position().top;
		var about = $("#about-div").position().top;

		if(scroll >= servicio){
			$(".menu-header .nav-item").removeClass("active");
			$(".menu-header .nav-item.servicio").addClass("active");
		}

		if(scroll >= comoTrabajo){
			$(".menu-header .nav-item").removeClass("active");
			$(".menu-header .nav-item.como-trabajo").addClass("active");
